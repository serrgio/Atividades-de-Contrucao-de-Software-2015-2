public class Algoritimo2 {

    /**
     * Verifica se determinado numero é par ou impar.
     * @param numero Numero inteiro a ser verificado.
     * @return true caso o numero seja par e false caso contrario.
     */
    public static boolean verificaParidade(int numero)
    {
        return (numero % 2 == 0);
    }
}
